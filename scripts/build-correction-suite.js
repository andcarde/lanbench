'use strict';

/**
 * @file Generates the AI-correction evaluation corpus from the single source of
 * truth `test-datasets/correction-suite.master.json`.
 *
 * For each size N in {10, 20, 30, 40} it writes:
 *   - `correction-N-input.xml`     — a WebNLG benchmark with the first N entries
 *     (triples + the flawed/valid Spanish candidate to correct), accepted by
 *     `utils/xml-reader.js#parseDatasetImport`.
 *   - `correction-N-expected.json` — the ground-truth verdicts for those entries
 *     (acceptance / warning / error + the expected code(s) and corrected form).
 *
 * The files nest: the 10-entry corpus is a prefix of the 20-entry one, etc.
 *
 * Usage: `node scripts/build-correction-suite.js`
 */

const fs = require('node:fs');
const path = require('node:path');

const CORPUS_DIR = path.join(__dirname, '..', 'test-datasets');
const MASTER_PATH = path.join(CORPUS_DIR, 'correction-suite.master.json');
const MASTER_50_PATH = path.join(CORPUS_DIR, 'correction-50-master.json');
const SIZES = [10, 20, 30, 40];

/**
 * Escapes XML reserved characters in a text node / attribute value.
 * @param {string} value
 * @returns {string}
 */
function escapeXml(value) {
    return String(value)
        .replaceAll('&', '&amp;')
        .replaceAll('<', '&lt;')
        .replaceAll('>', '&gt;')
        .replaceAll('"', '&quot;');
}

/**
 * Renders a single `<entry>` block in WebNLG benchmark form.
 * @param {Record<string, any>} entry
 * @returns {string}
 */
function renderEntry(entry) {
    const size = entry.triples.length;
    const tripleText = (/** @type {*} */ t) => escapeXml(`${t.subject} | ${t.predicate} | ${t.object}`);
    const otriples = entry.triples
        .map((/** @type {*} */ t) => `        <otriple>${tripleText(t)}</otriple>`)
        .join('\n');
    const mtriples = entry.triples
        .map((/** @type {*} */ t) => `        <mtriple>${tripleText(t)}</mtriple>`)
        .join('\n');

    return [
        `    <entry category="${escapeXml(entry.category)}" eid="${entry.eid}" shape="(X (X))" shape_type="NA" size="${size}">`,
        '      <originaltripleset>',
        otriples,
        '      </originaltripleset>',
        '      <modifiedtripleset>',
        mtriples,
        '      </modifiedtripleset>',
        `      <lex comment="" lang="en" lid="Id1">${escapeXml(entry.english)}</lex>`,
        `      <lex comment="candidate" lang="es" lid="Id1">${escapeXml(entry.candidate)}</lex>`,
        '    </entry>'
    ].join('\n');
}

/**
 * Renders the full benchmark XML for a slice of entries.
 * @param {Array<Record<string, any>>} entries
 * @returns {string}
 */
function renderInputXml(entries) {
    return [
        '<?xml version="1.0" ?>',
        '<benchmark>',
        '  <entries>',
        entries.map(renderEntry).join('\n'),
        '  </entries>',
        '</benchmark>',
        ''
    ].join('\n');
}

/**
 * Builds the expected-verdicts document for a slice of entries.
 * @param {number} size
 * @param {Array<Record<string, any>>} entries
 * @returns {Record<string, any>}
 */
function buildExpected(size, entries) {
    /** @type {Record<string, number>} */
    const counts = { ok: 0, warning: 0, error: 0 };
    for (const entry of entries)
        counts[entry.expectedSeverity] = (counts[entry.expectedSeverity] || 0) + 1;

    return {
        _doc: 'Ground-truth verdicts for the paired correction-N-input.xml. severity: ok = acceptance (no alert), warning = non-blocking, error = blocking. Generated by scripts/build-correction-suite.js — edit the master, not this file.',
        inputFile: `correction-${size}-input.xml`,
        entryCount: entries.length,
        counts,
        entries: entries.map(entry => ({
            eid: entry.eid,
            category: entry.category,
            candidate: entry.candidate,
            expectedSeverity: entry.expectedSeverity,
            expectedCodes: entry.expectedCodes,
            corrected: entry.corrected,
            rationale: entry.rationale
        }))
    };
}

/**
 * Generates all corpus files. Returns a short summary per size.
 * @returns {Array<{ size:number, ok:number, warning:number, error:number }>}
 */
function run() {
    const master = JSON.parse(fs.readFileSync(MASTER_PATH, 'utf8'));
    const all = master.entries;
    const summary = [];

    for (const size of SIZES) {
        const slice = all.slice(0, size);
        if (slice.length < size)
            throw new Error(`master has only ${all.length} entries, need ${size}`);

        const inputPath = path.join(CORPUS_DIR, `correction-${size}-input.xml`);
        const expectedPath = path.join(CORPUS_DIR, `correction-${size}-expected.json`);
        const expected = buildExpected(size, slice);

        fs.writeFileSync(inputPath, renderInputXml(slice), 'utf8');
        fs.writeFileSync(expectedPath, `${JSON.stringify(expected, null, 2)}\n`, 'utf8');

        summary.push({ size, ...expected.counts });
        console.log(`✔ correction-${size}-input.xml + correction-${size}-expected.json  (ok=${expected.counts.ok}, warning=${expected.counts.warning}, error=${expected.counts.error})`);
    }

    return summary;
}

/**
 * Generates the labelled 50-entry corpus from `correction-50-master.json`
 * (mix: 30 ok / 10 spelling / 10 fluency). It is standalone from the nested
 * 10/20/30/40 series because the mix is hand-tuned for the multi-provider
 * eval; the build script keeps both flows in one place so the test-datasets/
 * directory has a single owner.
 *
 * @returns {{ size:number, ok:number, warning:number, error:number }}
 */
function run50() {
    const master = JSON.parse(fs.readFileSync(MASTER_50_PATH, 'utf8'));
    const entries = master.entries;
    if (!Array.isArray(entries) || entries.length !== 50)
        throw new Error(`correction-50-master.json must hold exactly 50 entries (got ${entries.length})`);

    const inputPath = path.join(CORPUS_DIR, 'correction-50-input.xml');
    const expectedPath = path.join(CORPUS_DIR, 'correction-50-expected.json');
    const expected = buildExpected(50, entries);

    fs.writeFileSync(inputPath, renderInputXml(entries), 'utf8');
    fs.writeFileSync(expectedPath, `${JSON.stringify(expected, null, 2)}\n`, 'utf8');

    console.log(`✔ correction-50-input.xml + correction-50-expected.json  (ok=${expected.counts.ok}, warning=${expected.counts.warning}, error=${expected.counts.error})`);
    return { size: 50, ...expected.counts };
}

if (require.main === module) {
    run();
    run50();
}

module.exports = { run, run50, renderInputXml, buildExpected, escapeXml };
